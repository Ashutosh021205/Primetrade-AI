        "import pandas as pd\n",
        "import numpy as np\n",
        "\n",
        "def run_behavioral_analysis(merged_data_path):\n",
        "    df = pd.read_csv(merged_data_path)\n",
        "\n",
        "    # 1. Classify trades as Wins vs Losses based on Closed PnL\n",
        "    # If PnL is greater than 0, it's a win\n",
        "    df['is_win'] = df['Closed PnL'] > 0\n",
        "    df['is_loss'] = df['Closed PnL'] < 0\n",
        "\n",
        "    # Group by market sentiment state to check trading dynamics\n",
        "    summary = df.groupby('sentiment_class').agg(\n",
        "        total_trades=('Closed PnL', 'count'),\n",
        "        total_pnl=('Closed PnL', 'sum'),\n",
        "        avg_pnl=('Closed PnL', 'mean'),\n",
        "        avg_trade_size_usd=('Size USD', 'mean'),\n",
        "        win_rate=('is_win', lambda x: (x.sum() / (df.loc[x.index, 'Closed PnL'] != 0).sum()) * 100)\n",
        "    ).reset_index()\n",
        "\n",
        "    # Sort logically from most pessimistic to most optimistic market condition\n",
        "    sentiment_order = ['Extreme Fear', 'Fear', 'Neutral', 'Greed', 'Extreme Greed']\n",
        "    summary['sentiment_class'] = pd.Categorical(summary['sentiment_class'], categories=sentiment_order, ordered=True)\n",
        "    summary = summary.sort_values('sentiment_class').dropna(subset=['sentiment_class'])\n",
        "\n",
        "    print(summary.to_string(index=False, formatters={\n",
        "        'total_trades': '{:,}'.format,\n",
