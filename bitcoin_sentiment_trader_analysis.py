        "import pandas as pd\n",
        "import numpy as np\n",
        "from sklearn.model_selection import train_test_split\n",
        "from sklearn.preprocessing import LabelEncoder\n",
        "from sklearn.ensemble import RandomForestClassifier\n",
        "from sklearn.metrics import classification_report, roc_auc_score\n",
        "\n",
        "def train_profitability_predictor(merged_data_path):\n",
        "    df = pd.read_csv(merged_data_path)\n",
        "\n",
        "    # Target Variable: Is the trade strictly profitable? (1 = Win, 0 = Loss/Break-even)\n",
        "    df['is_win'] = (df['Closed PnL'] > 0).astype(int)\n",
        "\n",
        "    # Feature A: Extract time components from the transaction\n",
        "    df['parsed_time'] = pd.to_datetime(df['Timestamp IST'], format='%d-%m-%Y %H:%M')\n",
        "    df['trade_hour'] = df['parsed_time'].dt.hour\n",
        "    df['trade_day_of_week'] = df['parsed_time'].dt.dayofweek\n",
        "\n",
        "    # Feature B: Encode categorical text strings into numeric factors for the model\n",
        "    le_side = LabelEncoder()\n",
        "    df['encoded_side'] = le_side.fit_transform(df['Side'].astype(str))   # BUY vs SELL\n",
        "\n",
        "    le_coin = LabelEncoder()\n",
        "    df['encoded_coin'] = le_coin.fit_transform(df['Coin'].astype(str))   # BTC, ETH, etc.\n",
        "\n",
        "    # Feature C: Capture if the trader is trading against the sentiment momentum\n",
        "    # Example: Shorting (SELL) during Extreme Greed vs Buying (BUY) during Extreme Fear\n",
        "    df['contrarian_score'] = np.where(\n",
        "        (df['Side'] == 'BUY') & (df['sentiment_score'] < 30), 1,\n",
        "        np.where((df['Side'] == 'SELL') & (df['sentiment_score'] > 70), 1, 0)\n",
        "    )\n",
        "\n",
        "    feature_cols = [\n",
        "        'sentiment_score', 'Size USD', 'trade_hour',\n",
        "        'trade_day_of_week', 'encoded_side', 'encoded_coin', 'contrarian_score'\n",
        "    ]\n",
        "\n",
        "    # Drop rows that have missing values in our core features\n",
        "    df_ml = df.dropna(subset=feature_cols + ['is_win'])\n",
        "\n",
        "    X = df_ml[feature_cols]\n",
        "    y = df_ml['is_win']\n",
        "\n",
